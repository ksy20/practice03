package com.javaex.practice03;

public class Ex08 {

	public static void main(String[] args) {
		
		for (int dan=1; dan<=9; dan++) {
			
			for (int num=2; num<=9; num++) {
				
				System.out.print(num+"*"+dan+"="+dan*num+" ");
				System.out.print("\t");
			}
			
			System.out.println("");
		}

	}

}
